     public void tampil_data(){
    DefaultTableModel tabel=new DefaultTableModel();
    tabel.addColumn("NAMA");
    tabel.addColumn("GENDER");
    tabel.addColumn("ALAMAT");
    tabel.addColumn("POS");
    tabel.addColumn("HP");
    tabel.addColumn("EMAIL");
    tabel.addColumn("NO");
    try {
        java.sql.Connection conn = (java.sql.Connection)aplikasiuts.Koneksi1.koneksiTS();
String sql = "SELECT * FROM tsdata";
java.sql.PreparedStatement pst= conn.prepareStatement ( sql);
ResultSet rs = pst.executeQuery( sql);
      while(rs.next())
        {
        tabel.addRow(new Object[]{
            rs.getString(1),
            rs.getString(2),
            rs.getString(3),
            rs.getString(4),
            rs.getString(5),
            rs.getString(6),
            rs.getString(7)});
        }
        jTable1.setModel(tabel);
        }
    catch (Exception e){
    }
}

