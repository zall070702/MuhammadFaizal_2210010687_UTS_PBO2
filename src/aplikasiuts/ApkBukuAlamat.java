    private void tb_exportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tb_exportActionPerformed
        // Open file dialog to save the CSV file
    JFileChooser fileChooser = new JFileChooser();
    fileChooser.setDialogTitle("Save CSV File");
    fileChooser.setSelectedFile(new File("data_export.csv")); // Default file name
    
    int result = fileChooser.showSaveDialog(this);
    
    if (result == JFileChooser.APPROVE_OPTION) {
        File file = fileChooser.getSelectedFile();
        
        try (BufferedWriter bw = new BufferedWriter(new FileWriter(file))) {
            // Write the CSV header (columns)
            bw.write("NO,NAMA,GENDER,ALAMAT,POS,HP,EMAIL");
            bw.newLine();
            
            // Establish database connection
            java.sql.Connection conn = (java.sql.Connection)aplikasiuts.Koneksi1.koneksiTS();
            String sql = "SELECT NO, NAMA, GENDER, ALAMAT, POS, HP, EMAIL FROM tsdata";
            java.sql.Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            
            // Loop through the result set and write each row to the CSV file
            while (rs.next()) {
                String no = rs.getString("NO");
                String nama = rs.getString("NAMA");
                String gender = rs.getString("GENDER");
                String alamat = rs.getString("ALAMAT");
                String pos = rs.getString("POS");
                String hp = rs.getString("HP");
                String email = rs.getString("EMAIL");

                // Write the data to the CSV file
                bw.write(no + "," + nama + "," + gender + "," + alamat + "," + pos + "," + hp + "," + email);
                bw.newLine();
            }
            
            JOptionPane.showMessageDialog(null, "Data Exported Successfully!");
            conn.close();
        } catch (IOException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Failed to export data.");
            System.out.println(e.getMessage());
        }
    }
    }//GEN-LAST:event_tb_exportActionPerformed

