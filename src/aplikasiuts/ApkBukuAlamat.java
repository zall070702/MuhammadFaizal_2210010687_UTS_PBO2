    pst.setString(4, pos.getText());
    pst.setString(5, hp.getText());
    pst.setString(6, email.getText());  // Make sure the email field is set correctly
    pst.setInt(7, Integer.parseInt(no.getText()));
    
    // Menjalankan perintah update
    pst.executeUpdate();
    
